# --- 3. OUTPUT SCHEMA (ROBUST) ---
class PythonStrategyCode(BaseModel):
    class_parameters: Union[str, Dict[str, Any], List[Any]] = Field(
        description="Class variables. Can be a string, dict, or list."
    )
    init_indicators: Union[str, List[str]] = Field(
        description="Indicator lines. MUST use self.I() wrapper."
    )
    next_logic: Union[str, List[str]] = Field(
        description="Logic lines for per-candle execution."
    )
    explanation: str = Field(default="Generated by AI")

    # --- VALIDATORS ---
    @field_validator('class_parameters')
    @classmethod
    def normalize_params(cls, v):
        forbidden_keys = {"lambda", "class", "try", "except", "return", "def", "from", "import", "global", "pass", "None", "True", "False", "continue", "break", "if", "else", "elif", "for", "while", "in", "is", "not", "and", "or"}

        if isinstance(v, str):
            # If it's a string, we assume it's already formatted code.
            return v

        if isinstance(v, dict):
            lines = []
            for k, val in v.items():
                sanitized_k = f"{k}_" if k in forbidden_keys else k
                lines.append(f"{sanitized_k} = {val}")
            return "\n".join(lines)
        
        if isinstance(v, list):
            lines = []
            for item in v:
                if isinstance(item, str):
                    lines.append(item)
                elif isinstance(item, dict):
                    if "name" in item and "default" in item:
                        name = item['name']
                        val = item.get("default", item.get("value"))
                        sanitized_name = f"{name}_" if name in forbidden_keys else name
                        lines.append(f"{sanitized_name} = {val}")
                    elif "name" in item and "value" in item:
                         name = item['name']
                         sanitized_name = f"{name}_" if name in forbidden_keys else name
                         lines.append(f"{sanitized_name} = {item['value']}")
                    else:
                        for k, val in item.items():
                            sanitized_k = f"{k}_" if k in forbidden_keys else k
                            lines.append(f"{sanitized_k} = {val}")
            return "\n".join(lines)
            
        return v

    @field_validator('init_indicators')
    @classmethod
    def normalize_indicators(cls, v):
        if isinstance(v, list):
            return "\n".join(v)
        return v

    @field_validator('next_logic')
    @classmethod
    def normalize_logic(cls, v):
        if isinstance(v, list):
            return "\n".join(v)
        return v

