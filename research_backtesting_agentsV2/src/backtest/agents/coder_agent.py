def extract_json_str(text: str) -> str:
    # 1. Strip code fences
    text = re.sub(r"```json", "", text, flags=re.IGNORECASE)
    text = re.sub(r"```", "", text)
    text = text.strip()
    
    # 2. Try strict regex for outer braces
    match = re.search(r"(\{.*\})", text, re.DOTALL)
    if match: 
        return match.group(1)
    return text

def parse_code_response(raw_text: str) -> PythonStrategyCode:
    json_str = extract_json_str(raw_text)
    
    # Helper to unwrap nested responses
    def unwrap_if_nested(data: dict) -> dict:
        """
        AI sometimes wraps response in {"PythonStrategyCode": {...}}.
        This unwraps it to get the inner dict.
        """
        if "PythonStrategyCode" in data and isinstance(data["PythonStrategyCode"], dict):
            return data["PythonStrategyCode"]
        # Also check for lowercase variant
        if "pythonStrategyCode" in data and isinstance(data["pythonStrategyCode"], dict):
            return data["pythonStrategyCode"]
        return data
    
    try:
        # First Try: Standard JSON
        import json
        parsed = json.loads(json_str)
        parsed = unwrap_if_nested(parsed)
        return TypeAdapter(PythonStrategyCode).validate_python(parsed)
    except Exception as e1:
        try:
            # Second Try: Python Dict Evaluation (Soft JSON)
            py_str = json_str.replace("true", "True").replace("false", "False").replace("null", "None")
            py_dict = ast.literal_eval(py_str)
            py_dict = unwrap_if_nested(py_dict)
            return TypeAdapter(PythonStrategyCode).validate_python(py_dict)
        except Exception as e2:
            logging.error(f"JSON Parse Failed. Raw: {json_str[:100]}... Error: {e2}")
            raise ValueError(f"Failed to parse AI response. JSON Error: {e1} | AST Error: {e2}")

